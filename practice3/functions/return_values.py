# Example 1
def add(a, b):
    return a + b

result = add(2, 3)
print(result)

# Example 2
def square(num):
    return num * num

print(square(4))

# Example 3
def full_name(first, last):
    return f"{first} {last}"

print(full_name("John", "Doe"))

# Example 4
def is_positive(num):
    return num > 0

print(is_positive(-5))

# Example 5
def get_max(a, b):
    return max(a, b)

print(get_max(10, 20))
